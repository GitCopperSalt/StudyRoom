package com.project.demo.constant;

/**
 * 搜索筛选配置常量类
 * 
 * 本类定义了所有搜索和筛选操作中使用的常量参数名称
 * 用于统一前端请求参数和后端解析参数的一致性
 * 
 * 主要功能：
 * 1. 分页控制：page（页码）、size（页面大小）
 * 2. 条件筛选：like（模糊查询开关）、字段过滤
 * 3. 排序功能：orderby（排序字段）
 * 4. 字段选择：field（查询字段）
 * 5. 分组功能：groupby（分组字段）
 * 6. 范围查询：_min（最小值）、_max（最大值）
 * 
 * 使用方式：
 * 前端通过这些参数名传递查询条件，后端统一解析处理
 * 确保了搜索功能的标准化和可扩展性
 */
public class FindConfig {

    /**
     * 页码参数：指定查询结果的页数
     * 
     * 参数说明：
     * - 默认值：1（第一页）
     * - 数据类型：Integer
     * - 用途：分页查询时的页码标识
     * 
     * 计算公式：
     * 起始位置 = (page - 1) * size
     * 
     * 示例：
     * page=1, size=10 → 查询第1页，每页10条记录
     * page=2, size=10 → 查询第2页，每页10条记录（从第11条开始）
     */
    public static String PAGE = "page";

    /**
     * 页面大小参数：指定每页显示的记录数量
     * 
     * 参数说明：
     * - 默认值：10条记录
     * - 数据类型：Integer
     * - 用途：控制单页查询的记录数量
     * 
     * 性能考虑：
     * - 过小：需要多次请求，影响用户体验
     * - 过大：单次查询耗时长，影响系统性能
     * - 推荐值：10-50条记录
     * 
     * 安全限制：
     * - 建议设置最大值限制，防止查询过多数据
     * - 可以根据用户权限调整最大页面大小
     */
    public static String SIZE = "size";

    /**
     * 模糊查询开关：控制字符串字段的搜索方式
     * 
     * 参数说明：
     * - 默认值：null（使用模糊查询）
     * - 特殊值："0"（关闭模糊查询，使用精确匹配）
     * - 数据类型：String
     * 
     * 查询行为：
     * - null或非"0"：title LIKE '%keyword%'（前后模糊匹配）
     * - "0"：title = 'keyword'（精确匹配）
     * 
     * 性能影响：
     * - 模糊查询：无法使用索引，查询较慢
     * - 精确查询：可以使用索引，查询较快
     * 
     * 使用场景：
     * - 模糊查询：用户搜索、标题查找等
     * - 精确查询：主键查询、状态筛选等
     */
    public static String LIKE = "like";

    /**
     * 排序字段参数：指定查询结果的排序方式
     * 
     * 参数说明：
     * - 格式：字段名 [ASC|DESC]
     * - 默认值：null（使用数据库默认排序）
     * - 数据类型：String
     * 
     * 排序规则：
     * - ASC：升序排列（从小到大）
     * - DESC：降序排列（从大到小）
     * - 省略：默认升序
     * 
     * 示例：
     * - "create_time DESC"：按创建时间倒序
     * - "name ASC"：按名称升序
     * - "id"：按ID升序（默认）
     * 
     * 注意事项：
     * - 确保排序字段在数据库中存在
     * - 避免使用TEXT、BLOB等大字段排序
     * - 建议使用索引字段排序提高性能
     */
    public static String ORDER_BY = "orderby";

    /**
     * 查询字段参数：指定需要返回的数据字段
     * 
     * 参数说明：
     * - 默认值：null（返回所有字段）
     * - 格式：字段名1,字段名2,字段名3
     * - 数据类型：String
     * 
     * 性能优化：
     * - 指定字段：减少数据传输量，提高查询性能
     * - 避免SELECT *：减少I/O操作，提升查询速度
     * - 内存优化：减少对象创建，节省内存使用
     * 
     * 安全考虑：
     * - 敏感字段过滤：不在前端返回密码等敏感信息
     * - 字段权限：根据用户权限控制可查看的字段
     * 
     * 示例：
     * - "user_id,username,nickname"：只返回用户基本信息
     * - "id,title,content"：只返回文章核心信息
     */
    public static String FIELD = "field";

    /**
     * 分组字段参数：指定数据分组的字段
     * 
     * 参数说明：
     * - 用途：对查询结果进行分组统计
     * - 默认值：null（不分组）
     * - 数据类型：String
     * 
     * 分组功能：
     * - 统计分析：按类别、状态、时间等分组统计
     * - 数据聚合：SUM、COUNT、AVG等聚合函数
     * - 报表生成：生成各种统计报表
     * 
     * 常用场景：
     * - 按用户组统计用户数量
     * - 按状态统计订单分布
     * - 按时间统计注册趋势
     * 
     * 注意事项：
     * - 分组字段需要合理选择，避免过度分组
     * - 配合聚合函数使用，实现统计功能
     * - 注意分组后的数据量，避免内存溢出
     */
    public static String GROUP_BY = "groupby";

    /**
     * 最小值参数后缀：用于范围查询的最小值标识
     * 
     * 命名规则：
     * - 格式：字段名 + "_min"
     * - 示例：price_min（价格最小值）
     * 
     * 查询行为：
     * - 转换为：字段名 >= 最小值
     * - 用于：数值范围、时间范围等查询
     * 
     * 典型应用：
     * - 价格筛选：price_min=100, price_max=500
     * - 时间筛选：create_time_min=2024-01-01
     * - 年龄筛选：age_min=18, age_max=60
     * 
     * 数据处理：
     * - 自动解析参数名，提取基础字段名
     * - 转换为对应的SQL条件语句
     * - 支持数值、日期、时间等多种类型
     */
    public static String MIN_ = "_min";

    /**
     * 最大值参数后缀：用于范围查询的最大值标识
     * 
     * 命名规则：
     * - 格式：字段名 + "_max"
     * - 示例：price_max（价格最大值）
     * 
     * 查询行为：
     * - 转换为：字段名 <= 最大值
     * - 与_min配合使用实现范围查询
     * 
     * 典型应用：
     * - 价格范围：price_min=100, price_max=500
     * - 时间范围：create_time_max=2024-12-31
     * - 评分范围：rating_min=4.0, rating_max=5.0
     * 
     * 边界处理：
     * - 最大值必须大于等于最小值
     * - 边界值包含在查询范围内
     * - 处理边界异常情况
     */
    public static String MAX_ = "_max";
}
